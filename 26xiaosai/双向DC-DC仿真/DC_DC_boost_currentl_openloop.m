% ==============================================
% Boost电流内环开环传递函数伯德图绘制脚本（优化版）
% 功能：PI控制器 + 被控对象 + 1.5周期二阶Pade延时 级联开环伯德图
% 优化目标：相位裕度PM ≥ 45°，系统稳定
% 适用：同步Boost变换器电流内环s域稳定性分析
% 依赖：MATLAB Control System Toolbox
% ==============================================
clear; clc; close all;  % 清空工作区、命令行、关闭已有图窗

%% ===================== 1. 系统参数定义（可根据你的实际工况修改）=====================
% ---------------- 电路核心参数 ----------------
Uout = 50;               % 输出直流母线电压(V)，你的工况为50V
L = 2.2e-3;              % 升压储能电感值(H)，你的工况为2.2mH
Uin = 24;                % 输入蓄电池电压(V)，你的工况为20~26V，取中间值25V
Iout_max = 2;            % 最大输出电流(A)，你的工况为2A
R_load = 1e9;  % 满载负载电阻(Ω)

% ---------------- 开关频率与采样频率 ----------------
fsw = 10e3;              % 载波频率(Hz)，你的工况为10kHz（延时按这个算！）
Ts = 1/fsw;              % 载波周期(s) = 100μs
f_sample = 20e3;         % 采样频率(Hz)，你的工况为20kHz（过采样，不影响延时）
T_sample = 1/f_sample;   % 采样周期(s) = 50μs

% ---------------- 延时参数 ----------------
delay_cycle = 1.5;       % 延时周期数：1.5个载波周期
Td = delay_cycle * Ts;   % 总纯延时时间 = 1.5 * 100μs = 150μs

% ---------------- PI控制器参数（优化版，提升相位裕度）----------------
% 优化目标：截止频率ωc ≈ 1000 rad/s (≈159 Hz)，相位裕度PM ≥ 45°
Kp = 0.01;              % 比例系数（优化后）
Ki = 10;                % 积分系数（优化后）

%% ===================== 2. 构建s域各环节传递函数 =====================
% 1. 被控对象：忽略电感内阻的Boost电流环传递函数 G_id(s) = Uout/(L*s)
G_id = tf(Uout, [L 0]);  

% 2. PI控制器传递函数 G_c(s) = (Kp*s + Ki)/s
G_c = tf([Kp Ki], [1 0]);  

% 3. 1.5周期纯延时的二阶Pade逼近 G_delay(s) ≈ e^(-s*Td)
[num_delay, den_delay] = pade(Td, 2);  % 二阶Pade逼近
G_delay = tf(num_delay, den_delay);  

% 4. 级联得到完整开环传递函数 G_open(s) = G_c(s) * G_id(s) * G_delay(s)
G_open = G_c * G_id * G_delay;  

%% ===================== 3. 绘制开环伯德图 =====================
figure('Color','w','Name','Boost电流环开环伯德图（优化版）','Position',[100,100,900,650]);
% margin函数：绘制伯德图，同时标注相位裕度PM、增益裕度GM、截止频率
margin(G_open);  
grid on;  % 显示网格
title({'Boost电流内环开环传递函数伯德图（含1.5周期二阶Pade延时）'; ...
       ['优化参数：Kp=', num2str(Kp), ', Ki=', num2str(Ki)]}, ...
       'FontSize',12);
xlabel('频率 (rad/s)','FontSize',10);
set(gca,'FontSize',10);  % 坐标轴字体大小

%% ===================== 4. 控制台输出稳定性指标与设计提醒 =====================
% 计算系统稳定性指标
[GM, PM, Wcg, Wcp] = margin(G_open);

fprintf('==================== 系统稳定性指标（优化版）====================\n');
fprintf('增益裕度 GM: %.2f dB\n', 20*log10(GM));
fprintf('相位裕度 PM: %.2f °\n', PM);
fprintf('增益穿越频率(带宽) Wcg: %.2f rad/s (%.2f Hz)\n', Wcg, Wcg/(2*pi));
fprintf('相位穿越频率 Wcp: %.2f rad/s (%.2f Hz)\n', Wcp, Wcp/(2*pi));

% 计算Boost固有右半平面零点（稳定性核心约束）
D = 1 - Uin/Uout;  % 稳态占空比
sz = (1-D)^2 * R_load / L;  % 右半平面零点角频率
fz = sz/(2*pi);  % 右半平面零点频率

fprintf('\n==================== 设计约束提醒 ====================\n');
fprintf('Boost变换器固有右半平面零点频率: %.2f Hz\n', fz);
fprintf('当前电流环带宽(%.2f Hz) 远低于右半平面零点的1/5(%.2f Hz)，满足稳定要求\n', ...
        Wcg/(2*pi), fz/5);
fprintf('相位裕度PM=%.2f° ≥ 45°，系统稳定，可直接用于仿真\n', PM);
fprintf('如需更快响应，可适当增大Kp和Ki，但需保证PM≥45°\n');