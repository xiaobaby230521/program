% =========================================================================
% 脚本名称: svpwm_calc.m
% 功能说明: 根据已知参数生成 SVPWM 在单扇区内的规律采样作用时间比例数组。
% 三步法角度数组
% 参数配置: 
%   - n = 32 (扇区采样次数)
%   - Udc = 60V (直流母线电压)
%   - U_rms = 32V (期望输出线电压有效值)
%   - f_sw = 9.6kHz (计算所得开关频率)
% 计算公式依据: 参考图 image_cb5f81.png (公式 3-10, 3-11, 3-12)
% =========================================================================

clear; clc;

% 1. 基础参数定义
n = 32;                     % 采样次数
Udc = 60;                   % 直流母线电压
U_line_rms = 32;            % 期望线电压有效值

% 2. 计算幅值系数 a
Uref = sqrt(2) * (U_line_rms / sqrt(3)); 
a = (sqrt(3) * Uref) / Udc;

% 3. 生成角度采样点 (取采样周期的中心点位置)
theta = ((1:n) - 0.5) * (pi/3 / n);

% 4. 计算三个作用比例数组 (Qk, Qk+1, Q)
Qk = a * sin(pi/3 - theta);
Qk_plus_1 = a * sin(theta);
Q = a * cos(pi/6 - theta);

% 5. 命令行格式化输出 (方便直接复制)
fprintf('--- SVPWM 作用时间比例数组 (n=32, a=%.4f) ---\n\n', a);

fprintf('1. 矢量 Tk 作用比例数组 (Qk):\n');
fprintf('Qk = [%s];\n\n', num2str(Qk, '%.4f '));

fprintf('2. 矢量 Tk+1 作用比例数组 (Qk+1):\n');
fprintf('Qk_plus_1 = [%s];\n\n', num2str(Qk_plus_1, '%.4f '));

fprintf('3. 矢量总作用比例数组 (Q = Qk + Qk+1):\n');
fprintf('Q = [%s];\n\n', num2str(Q, '%.4f '));